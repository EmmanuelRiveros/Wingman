    private void cargarTablaVuelos() {
        DefaultTableModel modelo = (DefaultTableModel) tblVuelos.getModel();
        modelo.setRowCount(0);
        
        List<Vuelo> vuelos = Vuelo.obtenerTodos();
        for (Vuelo vuelo : vuelos) {
            modelo.addRow(new Object[]{
            vuelo.getId(),
            vuelo.getAvion().getNombre(),
            vuelo.getPiloto().getNombre(),
            vuelo.getDestino().getRuta(),
            vuelo.getTipoVuelo(),
            vuelo.getClima(),
            });
        }
    }
    
