public class Destino {

    public class destino {
      private int airbaseld;
        private String airbaseNombre;
        private Ruta Ruta;
        private String ubicacion;
        /**
         * @return the airbaseld
         */
        public int getAirbaseld() {
            return airbaseld;
        }

        /**
         * @param airbaseld the airbaseld to set
         */
        public void setAirbaseld(int airbaseld) {
            this.airbaseld = airbaseld;
        }

        /**
         * @return the airbaseNombre
         */
        public String getAirbaseNombre() {
            return airbaseNombre;
        }

        /**
         * @param airbaseNombre the airbaseNombre to set
         */
        public void setAirbaseNombre(String airbaseNombre) {
            this.airbaseNombre = airbaseNombre;
        }

        /**
         * @return the Ruta
         */
        public Ruta getRuta() {
            return Ruta;
        }

        /**
         * @param Ruta the Ruta to set
         */
        public void setRuta(Ruta Ruta) {
            this.Ruta = Ruta;
        }

        /**
         * @return the ubicacion
         */
        public String getUbicacion() {
            return ubicacion;
        }

        /**
         * @param ubicacion the ubicacion to set
         */
        public void setUbicacion(String ubicacion) {
            this.ubicacion = ubicacion;
        }
  
        
    }
}
